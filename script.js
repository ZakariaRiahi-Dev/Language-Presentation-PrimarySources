function buildTimeline(containerId, sportKey) {
  const el = document.getElementById(containerId);
  if (!el) return;
  el.innerHTML = SPORTS[sportKey].milestones.map(buildMilestoneHTML).join('');
}

function toggleMilestone(id) {
  const el = document.getElementById(id);
  if (!el) return;
  const wasOpen = el.classList.contains('open');
  el.parentElement.querySelectorAll('.milestone').forEach(s => s.classList.remove('open'));
  if (!wasOpen) el.classList.add('open');
}

function showSection(id, btn) {
  document.querySelectorAll('.sport-section').forEach(s => s.classList.remove('active'));
  document.querySelectorAll('.sport-nav button').forEach(b => b.classList.remove('active'));
  document.getElementById(id).classList.add('active');
  if (btn) btn.classList.add('active');
  const intro = document.getElementById('intro-text');
  if (intro) intro.style.display = (id === 'all-sports') ? '' : 'none';
}

// ── Image upload handling ──
function loadImage(input, zoneId) {
  const file = input.files[0];
  if (!file) return;
  const zone = document.getElementById(zoneId);
  const imgId = zoneId.replace('zone', 'img');
  const img = document.getElementById(imgId);
  const reader = new FileReader();
  reader.onload = e => {
    img.src = e.target.result;
    img.style.display = 'block';
    zone.classList.add('has-image');
    zone.querySelector('.img-upload-placeholder').style.display = 'none';
  };
  reader.readAsDataURL(file);
}

function clearImage(event, zoneId, imgId) {
  event.stopPropagation();
  const zone = document.getElementById(zoneId);
  const img = document.getElementById(imgId);
  img.src = ''; img.style.display = 'none';
  zone.classList.remove('has-image');
  zone.querySelector('.img-upload-placeholder').style.display = '';
  const fileInput = zone.querySelector('input[type=file]');
  fileInput.value = '';
}

// ── Summary table ──
function buildSummary() {
  const tbody = document.getElementById('summary-tbody');
  if (!tbody) return;
  const sorted = [...SUMMARY].sort((a, b) => parseInt(a.year) - parseInt(b.year));
  tbody.innerHTML = sorted.map(r => {
    const linkHtml = r.url
      ? `<a href="${r.url}" target="_blank" rel="noopener">🔗 View source</a>`
      : '';
    return `<tr>
      <td>${r.sport}</td>
      <td class="year-cell">${r.year}</td>
      <td>${r.milestone}</td>
      <td class="src-cell">${r.source}${linkHtml}</td>
    </tr>`;
  }).join('');
}

